    {
        $this->validate($request,[
            'name'=>'required'
        ]);

        $cate = new Category();
        $cate->name = Input::get('name');
        if(!$cate->save()){
            return Redirect::back()->withErrors('保存出错');
        }
        return Redirect::back();
    }

